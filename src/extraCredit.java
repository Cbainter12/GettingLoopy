public class extraCredit {
    public static void main(String[] args) {
        for (int x = 5; x >= 1; x--) {
            for (int y = 1; y <= 5; y++) {
                System.out.print("*");
            }
        }
        System.out.println();
        for (int x = 6; x >= 3; x--) {
            for (int y = 6; y <= 9; y++) {
                System.out.print("*");
            }
        }
    }
}

